public class Nombre {
    
    String valor;
    String cOrigen;
    String cDestino;
    String fIda;
    String fRegreso;
    String cantidad;

    public String getValor() {
        return valor;
    }

    public void setValor(String valor) {
        this.valor = valor;
    }

    public String getcOrigen() {
        return cOrigen;
    }

    public void setcOrigen(String cOrigen) {
        this.cOrigen = cOrigen;
    }

    public String getcDestino() {
        return cDestino;
    }

    public void setcDestino(String cDestino) {
        this.cDestino = cDestino;
    }

    public String getfIda() {
        return fIda;
    }

    public void setfIda(String fIda) {
        this.fIda = fIda;
    }

    public String getfRegreso() {
        return fRegreso;
    }

    public void setfRegreso(String fRegreso) {
        this.fRegreso = fRegreso;
    }

    public String getCantidad() {
        return cantidad;
    }

    public void setCantidad(String cantidad) {
        this.cantidad = cantidad;
    }
    
}
